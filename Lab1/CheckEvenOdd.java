public class CheckEvenOdd {
    public static void main(String[] args)
    {
        int number = 49;
        System.out.println("The number is " + number);
        if (number % 2 == 0)
        {
            System.out.println("This is an even number");
        }
        else
        {
            System.out.println("This is an odd number");
        }
    }
}